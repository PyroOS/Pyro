// SEE ALSO:
//----------------------------------------------------------------------------

int Alert::Go()
{
	uint32 nCode;
	int nError;

	m->m_hMsgPort = create_port( "alert_port", DEFAULT_PORT_SIZE );

	Show();
	MakeFocus();
	if( m->m_hMsgPort < 0 )
	{
		dbprintf( "Alert::WaitForSelection() failed to create message port\n" );
		PostMessage( M_QUIT );
		return ( -1 );
	}
	nError = get_msg( m->m_hMsgPort, &nCode, NULL, 0 );
	if( nError < 0 )
	{
		PostMessage( M_QUIT );
		return ( -1 );
	}
	else
	{
		PostMessage( M_QUIT );
		return ( nCode );
	}
}



